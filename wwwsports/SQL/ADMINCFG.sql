CREATE TABLE ADMINCFG 
(
  IADMIN_NO	INTEGER NOT NULL,
  CCATEGORY	VARCHAR(20) NOT NULL,
  CDISPLAY	VARCHAR(80) NOT NULL,
  CCMD_TYPE	VARCHAR(20) NOT NULL,
  CPARAM	VARCHAR(30),
CONSTRAINT ADMINCFG_PK PRIMARY KEY (IADMIN_NO)
);
grant all on ADMINCFG to public;
commit;

CREATE TABLE SVCCFG 
(
  ISVC_NO	INTEGER NOT NULL,
  CSVC_NAME	VARCHAR(50),
  CACCESS_RIGHT	VARCHAR(3),
CONSTRAINT SVCCFG_PK PRIMARY KEY (ISVC_NO)
);
grant all on SVCCFG to public;
commit;

